export function getLandingDict(locale: Locale): Dict {
  if (locale === 'zh') return zh
  if (locale === 'en') return en

  const page = getLocalePage(locale)
  const ui = uiByLocale[locale]

  if (!page || !ui) return en
  return buildDict(page, ui)
}
